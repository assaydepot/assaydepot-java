package com.assaydepot.conf;

import java.net.URL;

public class Configuration {

	private URL url;
	private String apiToken;
	
	public URL getUrl() {
		return url;
	}
	public void setUrl(URL url) {
		this.url = url;
	}
	public String getApiToken() {
		return apiToken;
	}
	public void setApiToken(String apiToken) {
		this.apiToken = apiToken;
	}
	
	
}
